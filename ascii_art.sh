#!/bin/bash
# ASCII Art Script with Colors

RED='\033[0;31m'
GREEN='\033[0;32m'
BLUE='\033[0;34m'
NC='\033[0m'

echo -e "${RED}*${GREEN}*${BLUE}*${NC} Welcome to CSC 2510 ${RED}*${GREEN}*${BLUE}*${NC}"
echo -e "${GREEN}   ____   ${NC}"
echo -e "${BLUE}  / __ \  ${NC}"
echo -e "${RED} / /  \ \ ${NC}"
echo -e "${GREEN} \ \__/ / ${NC}"
echo -e "${BLUE}  \____/  ${NC}"
